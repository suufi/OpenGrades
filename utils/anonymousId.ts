function hashCode(str: string): number {
  let hash = 0
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) - hash + str.charCodeAt(i)) | 0
  }
  return Math.abs(hash)
}

export function hashToColor(id?: string): string {
  if (!id) return 'var(--mantine-color-gray-4)'
  const h = hashCode(id) % 360
  return `hsl(${h}, 90%, 80%)`
}

export function getAnonLabel(id?: string): { name: string; initial: string } {
  if (!id) return { name: 'Anonymous', initial: '?' }
  const animal = ANON_ANIMALS[hashCode(id) % ANON_ANIMALS.length]
  return { name: `Anonymous ${animal}`, initial: animal[0] }
}
