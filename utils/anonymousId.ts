import crypto from 'crypto'

const ANONYMOUS_ID_LENGTH = 6

export function getAnonymousId(userId: unknown, seed: string = ''): string {
  if (userId == null) return ''
  const str = typeof userId === 'string' ? userId : (userId as { toString(): string }).toString()
  const hash = crypto.createHash('sha256').update(str + seed).digest('hex')
  return hash.slice(0, ANONYMOUS_ID_LENGTH)
}

